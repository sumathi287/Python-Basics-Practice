# Create a set and print it.
var_set = {1, 2, 3, 4, 5, 7, 8}
print(var_set)
# Add a new element to a set.
var_set.add("a")
print(var_set)
# Remove an element from a set.
var_set.remove("a")
print(var_set)
# Check whether an element exists in a set.
if 1 in var_set:
    print("yes")
# Count the total elements in a set.
total_element = len(var_set)
print(total_element)
