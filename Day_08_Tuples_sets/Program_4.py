# Print the first and last element of a tuple.
tup = (1, 2, 3, 4, 5)
print(f"The first element:{tup[0]}\nThe last element:{tup[-1]}")
#Find the length of a tuple.
print(len(tup))
#Count how many times a number appears in a tuple.
tup = (1, 2, 3, 4, 5,5)
print(tup.count(5))
#Find the index position of a given element.
print(tup.index(4))